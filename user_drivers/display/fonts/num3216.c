
#include "num3216.h"
#include "display.h"
#include "sys_err.h"

static const u8 Font_Num3216_1[];
static const u8 Font_Num3216_2[];
static const u8 Font_Num3216_3[];
static const u8 Font_Num3216_4[];
static const u8 Font_Colon3216[];

static i2c_oled_font_t font_num3216 = {
    .data = Font_Num3216_1,
    .size = 10, // 0-9
    .width = 16,
    .height = 32,
};

/**
 * @brief Show a single digit (0-9) in 32x16 font on the OLED display. The font style can be selected by `font_index`.
 * 
 * @param row y coordinate in rows (1 row = 8 pixels), range 0 ~ (height / 8 - 1)
 * @param col x coordinate in cols, range 0 ~ (width - 1)
 * @param num The digit (0-9) to display.
 * @param font_index The index of the font style to use.
 *            It should be one of the values defined in `font_num3216_index_t`.
 */
void oled_show_num3216(u8 row, u8 col, u8 num, font_num3216_index_t font_index)
{
    CHECK_FALSE_RET_VOID(num < 10 && font_index < FONT_NUM3216_MAX);

    switch (font_index) {
        case FONT_NUM3216_1:
            font_num3216.data = Font_Num3216_1;
            break;
        case FONT_NUM3216_2:
            font_num3216.data = Font_Num3216_2;
            break;
        case FONT_NUM3216_3:
            font_num3216.data = Font_Num3216_3;
            break;
        case FONT_NUM3216_4:
            font_num3216.data = Font_Num3216_4;
            break;
        case FONT_COLON3216:
            font_num3216.data = Font_Colon3216;
            i2c_oled_show_char(&oled, row, col, &font_num3216, 0);
            return;
        default:
            return;
    }

    i2c_oled_show_char(&oled, row, col, &font_num3216, num);
}


